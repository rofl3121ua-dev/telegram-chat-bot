def detect_lang(text: str) -> Lang:
    if not text:
        return "uk"
    normalized = text.lower()
    if any(ch in normalized for ch in ("ї", "є", "і", "ґ")):
        return "uk"

    uk_hits = sum(
        1
        for token in ("привіт", "дякую", "будь", "чому", "коли", "тобі", "мені", "це", "ти", "що")
        if token in normalized
    )
    ru_hits = sum(
        1
        for token in ("привет", "спасибо", "почему", "когда", "тебе", "мне", "это", "что")
        if token in normalized
    )
    if uk_hits > ru_hits:
        return "uk"
    if ru_hits > uk_hits:
        return "ru"
    return "uk"


def t(lang: Lang, key: str, **kwargs: str) -> str:
    template = TRANSLATIONS.get(lang, TRANSLATIONS["uk"]).get(key, key)
    return template.format(**kwargs)
