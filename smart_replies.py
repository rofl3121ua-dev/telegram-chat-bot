from __future__ import annotations

import random
import re
from dataclasses import dataclass
from typing import Optional


def _words(text: str) -> list[str]:
    return re.findall(r"[a-zа-яіїєґё']+", text.lower())


def _matches(normalized: str, trigger: str) -> bool:
    """Короткие триггеры (до 4 символов) — только целое слово или всё сообщение, чтобы не ловить «ок» в «окно»."""
    t = trigger.lower().strip()
    if not t:
        return False
    if len(t) >= 5:
        return t in normalized
    if normalized.strip() == t:
        return True
    return t in _words(normalized)


@dataclass(frozen=True)
class _Rule:
    priority: int
    triggers: tuple[str, ...]
    ru: tuple[str, ...]
    uk: tuple[str, ...]


def _pick(ru: tuple[str, ...], uk: tuple[str, ...], lang: str) -> str:
    pool = uk if lang == "uk" else ru
    return random.choice(pool)


